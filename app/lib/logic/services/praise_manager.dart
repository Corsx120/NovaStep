import 'dart:math';

class PraiseManager {
  static final List<String> _phrases = [
    "Ого! Ты просто машина продуктивности! 🚀",
    "Минус одна задача - плюс сто к уверенности! 💪",
    "NovaStep гордится тобой! Продолжай в том же духе. ✨",
    "Это было мощно! Пора заварить чаек? ☕",
    "Ты на шаг ближе к своей цели. Так держать! 🎯",
    "Еще одна победа в копилку! Сегодня твой день. 🌟",
    "Твоя продуктивность просто зашкаливает! 🔥",
    "Сделано! А ты хорош! 😉",
    "Маленький шаг для человека, огромный шаг для твоего успеха! 🛰️",
    "Задачи трепещут перед тобой! ⚔️",
  ];

  static String getRandomPraise() {
    return _phrases[Random().nextInt(_phrases.length)];
  }

  // Фраза для перерыва
  static String getBreakMessage(int count) {
    return "Ого, уже $count задач за сегодня! 🤯 Пора сделать перерыв на 10-15 минут, чтобы перезагрузиться. Ты это заслужил! 🍵";
  }

  // Фраза для вечернего итога
  static String getEveningSummary(int count) {
    if (count == 0) return "Сегодня задач не закрыто, но завтра — новый день и новые возможности! Отдыхай! 🌙";
    if (count < 3) return "Сегодня закрыто $count задач. Маленькими шагами к большой цели! Доброй ночи! 😴";
    return "Вау! Сегодня ты закрыл целых $count задач! Ты настоящий герой дня. Теперь время заслуженного отдыха! 🏆🌙";
  }
}